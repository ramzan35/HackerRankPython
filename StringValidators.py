s = input()

if(s.isalnum()):
    print("TRUE")
else:
    print("FALSE")

if(s.isalpha()):
    print("TRUE")
else:
    print("FALSE")

if(s.isdigit()):
    print("TRUE")
else:
    print("FALSE")

if(s.islower()):
    print("TRUE")
else:
    print("FALSE")

if(s.isupper()):
    print("TRUE")
else:
    print("FALSE")
